# Adição
print(10 + 2)

# Subtração
print(10 - 2)

# Multiplocação
print(10 * 2)

# Divisão
print(10 / 2)
# Retorna um FLOAT

# Divisão inteira
print(10 // 2)
# Retorna um INT

# Módulo
print(10 % 3)
# Retorna o resto da divisão

# Exponenciação
print(10 ** 3)
# Mesmo que potenciação, ou seja, 10 elevado a 3 