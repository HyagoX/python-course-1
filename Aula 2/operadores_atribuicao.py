# Atribuição Simples
saldo = 500

print(saldo)

# Tambem funciona para substituir o valor de uma variável existente
saldo = 200

print(saldo)

# Atribuição com Adição
saldo += 200

print(saldo)

# Atribuição com subtração
saldo -= 100

print(saldo)

# Atribuição com multiplicação
saldo *= 2

print(saldo)

# Atribuição com divisão
saldo /= 3

print(saldo)

# Atribuição com divisão Inteira
saldo //= 2

print(saldo)

# Atribuição com Módulo
saldo %= 2

print(saldo)

# Atribuição com Exponenciação
saldo **= 3

print(saldo)