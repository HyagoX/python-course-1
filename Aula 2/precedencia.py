# A linguagem Python realiza os calculos com base as regras básicas da matemática, primeiro oque está em parenteses, em segundo as exponenciações, terceiro multiplicações e divisões (Da esquerda para a direita), e por ultimo somas e subtrações (Tambem da esquerda para direita)
print(10 - 5 * 2)

print((10 - 5) * 2)

print(10 ** 2 * 2)

print(10 ** (2 * 2))

print(10 ** (2 * 2))

